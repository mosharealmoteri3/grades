#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
نظام تقييم درجات الطلاب
----------------------
هذا البرنامج يأخذ درجة الطالب (0-100) ويعرض التقدير المناسب.
يمكن للمستخدم إدخال درجات متعددة حتى يكتب كلمة "خروج".
"""

def get_grade_letter(score: float) -> str:
    """
    تحويل الدرجة الرقمية إلى تقدير نصي
    
    المعاملات:
        score (float): درجة الطالب بين 0 و 100
        
    المخرجات:
        str: التقدير المناسب (ممتاز، جيد جداً، جيد، مقبول، راسب)
    """
    if score >= 90:
        return "ممتاز (A)"
    elif score >= 80:
        return "جيد جداً (B)"
    elif score >= 70:
        return "جيد (C)"
    elif score >= 60:
        return "مقبول (D)"
    else:
        return "راسب (F)"


def validate_score(score: float) -> bool:
    """
    التحقق من صحة الدرجة (بين 0 و 100)
    
    المعاملات:
        score (float): درجة الطالب
        
    المخرجات:
        bool: True إذا كانت الدرجة صحيحة، False إذا كانت خارج النطاق
    """
    return 0 <= score <= 100


def main():
    """الدالة الرئيسية لتشغيل البرنامج"""
    
    # عرض رسالة الترحيب
    print("\n" + "=" * 50)
    print("    نظام تقييم درجات الطلاب")
    print("=" * 50)
    print("أدخل درجة الطالب (0-100) أو اكتب 'خروج' لإنهاء البرنامج.\n")
    
    while True:
        # طلب إدخال المستخدم
        user_input = input("أدخل درجة الطالب: ").strip()
        
        # التحقق من طلب الخروج (بدون حساسية لحالة الأحرف)
        if user_input.lower() == "خروج":
            print("\nشكراً لاستخدام البرنامج. مع السلامة!")
            break
        
        # محاولة تحويل المدخلات إلى رقم مع التعامل مع الأخطاء
        try:
            score = float(user_input)
        except ValueError:
            # التعامل مع الإدخال غير الرقمي
            print("❌ خطأ: الرجاء إدخال رقم صحيح بين 0 و 100 أو كلمة 'خروج'.\n")
            continue
        
        # التحقق من صحة الدرجة (النطاق 0-100)
        if not validate_score(score):
            print(f"❌ خطأ: الدرجة {score} غير صالحة. الرجاء إدخال درجة بين 0 و 100.\n")
            continue
        
        # حساب التقدير وعرضه للمستخدم
        grade = get_grade_letter(score)
        print(f"✅ تقدير الطالب للدرجة {score}: {grade}\n")
        print("-" * 40)


if __name__ == "__main__":
    main()
